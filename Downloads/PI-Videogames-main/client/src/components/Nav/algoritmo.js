// a-z z-a
function ordenar(array) {
    for (let i = 1; i < array.length; i++){
      for (let j = i; j > 0; j--){
        if (array[j].name[0] < array[j-1].name[0]){
        let temp = array[j]
        array[j] = array[j-1]
        array[j-1] = temp
      }
    }
  }
    return array
}

